def _extract_framework_from_answer(answer_text: str) -> list:
    """Extract key bullet points from a full answer for use as framework."""
    lines = answer_text.split("\n")
    bullets = []
    for line in lines:
        line = line.strip()
        if line.startswith("- ") or line.startswith("• ") or line.startswith("* "):
            bullets.append(line[2:].strip())
        elif line.startswith("**") and line.endswith("**"):
            pass  # skip headings
        elif len(line) > 40 and not line.startswith("#"):
            # Prose sentences — take first clause
            bullets.append(line[:120])
    return bullets[:10]


